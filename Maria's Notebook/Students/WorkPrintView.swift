/// Sheet wrapper for presenting the print interface
struct WorkPrintSheet: View {
    let workItems: [WorkModel]
    let students: [Student]
    let lessons: [Lesson]
    let filterDescription: String
    let sortDescription: String
    
    @Environment(\.dismiss) private var dismiss
    
    var body: some View {
        #if os(iOS)
        VStack(spacing: 0) {
            // Preview the print view
            ScrollView {
                WorkPrintView(
                    workItems: workItems,
                    students: students,
                    lessons: lessons,
                    filterDescription: filterDescription,
                    sortDescription: sortDescription
                )
                .frame(width: 612 * 0.5, height: 792 * 0.5) // 50% scale for preview
                .scaleEffect(0.5)
                .frame(width: 612 * 0.5, height: 792 * 0.5)
            }
            .background(Color.gray.opacity(0.1))
            
            Divider()
            
            HStack {
                Button("Cancel") {
                    dismiss()
                }
                
                Spacer()
                
                Button("Print", systemImage: "printer") {
                    presentPrint()
                }
                .keyboardShortcut(.defaultAction)
            }
            .padding()
        }
        #else
        VStack(spacing: 16) {
            HStack {
                Text("Print Preview")
                    .font(.headline)
                Spacer()
                Button("Close") {
                    dismiss()
                }
            }
            
            ScrollView {
                WorkPrintView(
                    workItems: workItems,
                    students: students,
                    lessons: lessons,
                    filterDescription: filterDescription,
                    sortDescription: sortDescription
                )
                .frame(width: 612 * 0.6, height: 792 * 0.6)
                .scaleEffect(0.6)
                .frame(width: 612 * 0.6, height: 792 * 0.6)
            }
            .background(Color.gray.opacity(0.1))
            
            HStack {
                Button("Cancel") {
                    dismiss()
                }
                
                Spacer()
                
                Button("Print", systemImage: "printer") {
                    presentPrint()
                }
                .keyboardShortcut(.defaultAction)
            }
        }
        .padding()
        .frame(minWidth: 500, minHeight: 700)
        #endif
    }
    
    private func presentPrint() {
        #if os(iOS)
        let renderer = ImageRenderer(content: WorkPrintView(
            workItems: workItems,
            students: students,
            lessons: lessons,
            filterDescription: filterDescription,
            sortDescription: sortDescription
        ).frame(width: 612, height: 792))
        
        if let pdfData = renderer.pdf() {
            let printController = UIPrintInteractionController.shared
            printController.printingItem = pdfData
            
            let printInfo = UIPrintInfo.printInfo()
            printInfo.outputType = .general
            printInfo.jobName = "Open Work Report"
            printController.printInfo = printInfo
            
            printController.present(animated: true) { _, completed, error in
                if completed {
                    dismiss()
                }
            }
        }
        #else
        let printView = WorkPrintView(
            workItems: workItems,
            students: students,
            lessons: lessons,
            filterDescription: filterDescription,
            sortDescription: sortDescription
        )
        
        let hostingView = NSHostingView(rootView: printView.frame(width: 612, height: 792))
        hostingView.frame = CGRect(x: 0, y: 0, width: 612, height: 792)
        
        let printOperation = NSPrintOperation(view: hostingView)
        printOperation.printInfo.topMargin = 36
        printOperation.printInfo.bottomMargin = 36
        printOperation.printInfo.leftMargin = 36
        printOperation.printInfo.rightMargin = 36
        
        printOperation.runModal(for: NSApp.keyWindow!, delegate: nil, didRun: nil, contextInfo: nil)
        dismiss()
        #endif
    }
}

// MARK: - Extensions

extension WorkKind {
    var displayName: String {
        switch self {
        case .practiceLesson: return "Practice"
        case .followUp: return "Follow Up"
        case .report: return "Report"
        case .research: return "Research"
        case .choice: return "Choice"
        }
    }
}

extension WorkStatus {
    var displayName: String {
        switch self {
        case .active: return "Active"
        case .review: return "Review"
        case .complete: return "Complete"
        }
    }
}
